# string defination:
#'' or " can be used to define a string
#''' can be used to define a string that spans multiple lines

variable = "abcdefghij"
print(variable[1:3])  # string slicing


# forward and reverse indexing:
# forward indexing starts from 0 and reverse indexing starts from -1

str = "aditi"
print(str[0])  # forward indexing
print(str[-1])  # reverse indexing

# length of a string:
# the length of a string can be found using the len() function

str = "aditi"
print(len(str))


# string concatenation:
# string concatenation is the process of combining two or more strings into one string.

s1 = "aditi"
s2 = "jagadale"
s3 = s1 + " " + s2
print(s3)


# string repetition * :
# string repetition is the process of repeating a string a certain number of times.

print("hieee" * 3)
