# range and step
for i in range(1, 10, 2):
    print(i)


# list
l = [1, 2, 3, 4, 5]
for i in l:
    print(i)


# str
str = "xyz"
for i in str:
    print(i)


# while loop
a = 10  # approx value
while a <= 10:
    print(a)
    a += 1  # finite incremental loop


# infinite loop
while True:
    print("abcd")
